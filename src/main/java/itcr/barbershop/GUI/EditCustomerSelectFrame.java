    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    // Obtener el cliente seleccionado del JList
    String selectedCustomerString = customerList.getSelectedValue();
    if (selectedCustomerString != null) {
        // Convertir la cadena seleccionada a un objeto Customer
        int customerId = Integer.parseInt(selectedCustomerString.split(":")[0].trim()); 
        Customer selectedCustomer = null;
        try {
            selectedCustomer = AppointmentManager.getInstance().getCustomer(customerId);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            return;
        }
        
        // Crear una instancia de EditCustomerFrame y pasar el cliente seleccionado
        EditCustomerFrame editCustomerFrame = new EditCustomerFrame(selectedCustomer);
        editCustomerFrame.setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione un cliente antes de continuar.");
    }     
    }//GEN-LAST:event_jButton1ActionPerformed

