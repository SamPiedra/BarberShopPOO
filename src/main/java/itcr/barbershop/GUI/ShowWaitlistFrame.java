    private void populateWaitlist() {
        ArrayList<String> waitlistData = AppointmentManager.getInstance().getWaitingList();
        
        if (waitlistData.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay clientes en lista de espera.");
        } else {
            for (String entry : waitlistData) {
                listModel.addElement(entry);
            }
        }
    }

