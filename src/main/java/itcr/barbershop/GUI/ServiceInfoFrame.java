    private void loadServices() {
        try {
            ArrayList<String> serviceTypesList = manager.getServiceTypesList();
            for (String serviceType : serviceTypesList) {
                listModel.addElement(serviceType);
            }
        } catch (Exception ex) {
        }
    }

